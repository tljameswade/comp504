package sq6_kt21.server.game.model.message;


import java.util.UUID;

import common.msg.IChatMsg;

/**
 * The cityMsg class
 * @author suozhiqi
 *
 */
public class CityMsg implements IChatMsg{

	/**
	 * 
	 */
	private static final long serialVersionUID = -4860731301812235236L;
	
	private int total;

	private UUID id;
	public CityMsg(UUID id, int total) {
		this.setTotal(total);
	}

	public int getTotal() {
		return total;
	}


	public void setTotal(int total) {
		this.total = total;
	}

	public UUID getId() {
		return id;
	}

	public void setId(UUID id) {
		this.id = id;
	}


}
